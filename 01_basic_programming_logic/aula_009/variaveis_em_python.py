# Exemplo 1: variável do tipo string (texto)
nome = "Uncle Bob"
print(nome)

# Exemplo 2: usando variável dentro de uma string (f-string)
mensagem = f"Olá, meu nome é {nome}"
print(mensagem)

# Exemplo 3: variável do tipo inteiro
idade = 72

# Exemplo 4: expressão gerando um valor booleano
maior_de_idade = idade >= 18
print(maior_de_idade)

# Exemplo 5: usando valores calculados em variáveis
ano_atual = 2026
ano_nascimento = ano_atual - idade
print(ano_nascimento)

# Exemplo 6: variáveis deixam o código mais legível
criador_do_python = "Guido van Rossum"
print(criador_do_python)

# Exemplo 7: evitando repetição de valores
numero = 10
dobro = numero * 2
print(dobro)

# Exemplo 8: perguntando algo ao programa
tem_experiencia = idade >= 30
print(tem_experiencia)