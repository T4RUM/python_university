
# int -> números inteiros
print(11)
print(-11)
print(0)

# float -> números com ponto flutuante
print(1.1)
print(1.1, 10.11)  # dois argumentos
print(0.0)
print(-1.5)

# Descobrindo tipos com type()
print(type(1.5))      # float
print(type(5))        # int
print(type("Guido"))  # str
print(type(-1))       # int
print(type(-1.1))     # float
print(type(0))        # int

# Também funciona com múltiplos valores
print(type(1.1), type("Guido"), type(0))